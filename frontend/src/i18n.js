export function translate(lang, key, vars) {
  let s = (translations[lang] && translations[lang][key]) ?? translations.tr[key] ?? key;
  if (vars) {
    for (const k in vars) s = s.split(`{${k}}`).join(String(vars[k]));
  }
  return s;
}

// In components: const t = useT(); t("key", {vars}). Re-renders automatically on language change.
export function useT() {
  const lang = useLang();
  return (key, vars) => translate(lang, key, vars);
}
