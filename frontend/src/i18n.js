// Lightweight i18n: a module-level language store + subscription via useSyncExternalStore.
// No provider needed — components just do `const t = useT()` and call `t("key")`;
// when the language changes, subscribed components re-render. The language persists in localStorage.
import { useSyncExternalStore } from "react";

const LANG_KEY = "calivi_lang";

export const LANGUAGES = [
  { code: "tr", label: "Türkçe" },
  { code: "en", label: "English" },
  { code: "de", label: "Deutsch" },
  { code: "es", label: "Español" },
  { code: "it", label: "Italiano" },
  { code: "pt", label: "Português" },
  { code: "ru", label: "Русский" },
  { code: "ja", label: "日本語" },
  { code: "zh", label: "中文" },
];

let currentLang = localStorage.getItem(LANG_KEY) || "tr";
const listeners = new Set();

export function getLang() {
  return currentLang;
}

export function setLang(lang) {
  if (lang === currentLang) return;
  currentLang = lang;
  localStorage.setItem(LANG_KEY, lang);
  listeners.forEach((l) => l());
}

function subscribe(cb) {
  listeners.add(cb);
  return () => listeners.delete(cb);
}

export function useLang() {
  return useSyncExternalStore(subscribe, getLang);
}

